package com.example.sahdowlayout;

public class Shadow {
    private int blur;
    private String color;
    private int alpha;
    private int x;
    private int y;


    public int getBlur() {
        return blur;
    }

    public String getColor() {
        return color;
    }

    public int getAlpha() {
        return alpha;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setBlur(int blur) {
        this.blur = blur;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setAlpha(int alpha) {
        this.alpha = alpha;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

}
